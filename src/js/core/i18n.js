  function getLanguage() {
    if (game.state && game.state.game && game.state.game.settings && game.state.game.settings.language) {
      return game.state.game.settings.language;
    }
    return "zh-CN";
  }

  function template(text, vars) {
    return String(text || "").replace(/\{\{(.*?)\}\}/g, function (match, key) {
      var trimmed = String(key || "").trim();
      return vars && vars[trimmed] !== undefined ? vars[trimmed] : "";
    });
  }

  function t(key, vars) {
    var language = getLanguage();
    var localePack = messages[language] || messages["zh-CN"];
    var fallbackPack = messages["zh-CN"];
    return template(localePack[key] || fallbackPack[key] || key, vars);
  }

  function getDataText(record, field) {
    var language = getLanguage();
    if (language === "ja" && record && record[field + "Ja"]) {
      return record[field + "Ja"];
    }
    if (language === "ja" && record && record[field + "En"]) {
      return record[field + "En"];
    }
    if (language === "en" && record && record[field + "En"]) {
      return record[field + "En"];
    }
    return record ? record[field] : "";
  }

